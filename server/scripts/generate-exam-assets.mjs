import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..', '..');
const examModuleDir = path.join(repoRoot, 'server', 'src', 'modules', 'exam-simulator');

const quizQuestionsModuleUrl = pathToFileURL(path.join(repoRoot, 'src', 'data', 'quizQuestions.js')).href;
const practicalExamModuleUrl = pathToFileURL(path.join(repoRoot, 'src', 'data', 'practicalExam.js')).href;

const { SAMPLE_QUESTIONS } = await import(quizQuestionsModuleUrl);
const {
  PRACTICAL_EXAM_TYPES,
  PRACTICAL_SWIM_EXAMS
} = await import(practicalExamModuleUrl);

const theoryQuestionBank = Object.fromEntries(
  Object.entries(SAMPLE_QUESTIONS || {}).filter(([categoryId]) => categoryId !== 'guess')
);

const fileHeader = `/**
 * This file is generated by server/scripts/generate-exam-assets.mjs.
 * Do not edit manually; regenerate from the frontend source question bank.
 */
`;

const theoryOutput = `${fileHeader}
export const THEORY_QUESTION_BANK = ${JSON.stringify(theoryQuestionBank, null, 2)} as const;
`;

const practicalOutput = `${fileHeader}
export const PRACTICAL_EXAM_TYPES = ${JSON.stringify(PRACTICAL_EXAM_TYPES, null, 2)} as const;

export const PRACTICAL_SWIM_EXAMS = ${JSON.stringify(PRACTICAL_SWIM_EXAMS, null, 2)} as const;
`;

await mkdir(examModuleDir, { recursive: true });
await writeFile(path.join(examModuleDir, 'theory-question-bank.generated.ts'), theoryOutput, 'utf8');
await writeFile(path.join(examModuleDir, 'practical-exam-data.generated.ts'), practicalOutput, 'utf8');

process.stdout.write('Generated server exam assets.\n');
